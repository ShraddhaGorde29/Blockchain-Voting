    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name=jTextField1.getText();
        String des=jTextField2.getText();
        String email=jTextField3.getText();
        String mob=jTextField4.getText();
        EditBureacratsMaker ab=new EditBureacratsMaker();
        if(name.isEmpty())
        JOptionPane.showMessageDialog(null,"NAME MANDOTORY");
        else if(des.isEmpty())
        JOptionPane.showMessageDialog(null,"NAME MANDOTORY");
        else if(!isValidEmail(email))
        {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Email ID");
        }
        else  if(!isValidMobile(mob))

        JOptionPane.showMessageDialog(null,"INVALID HOSPITAL MOBILE NUMBER OR EMPTY");
        else
        {

            if(ab.updateBureacats(name, des, email, mob))
            {

                JOptionPane.showMessageDialog(null,"BUREAUCRATS INFO UPDATED SUCESSFULLY ");

            }else
            JOptionPane.showMessageDialog(null,"ERROR IN ADDING PLEASE TRY AGAIN ");

        }

    }//GEN-LAST:event_jButton1ActionPerformed

